package Java_training;

public class Switch_Case {
	public static void main(String[] args) {
		switch (2) {
		case 1:{
			System.out.println("c1");
			break;
		}
		case 2:{
			System.out.println("c2");
			break;
		}
		case 3:{
			System.out.println("c3");
			break;
		}
		case 4:{
			System.out.println("c4");
			break;
		}
		default:{
			System.out.println("no match");
		}
	}
}
}
